package _6_hoework;

public class ychilishte extends kyshta{
	
	public ychilishte(String vrata){
		super(vrata);
	}
	
	private String stylbishte;
	private int etaji;
	/**
	 * @return the stylbishte
	 */
	public String getStylbishte() {
		return stylbishte;
	}
	/**
	 * @param stylbishte the stylbishte to set
	 */
	public void setStylbishte(String stylbishte) {
		this.stylbishte = stylbishte;
	}
	/**
	 * @return the etaji
	 */
	public int getEtaji() {
		return etaji;
	}
	/**
	 * @param etaji the etaji to set
	 */
	public void setEtaji(int etaji) {
		this.etaji = etaji;
	}

}

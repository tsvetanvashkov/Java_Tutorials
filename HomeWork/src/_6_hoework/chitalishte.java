package _6_hoework;

public class chitalishte extends kyshta{

	public chitalishte(String vrata){
		super(vrata);
	}
	
	private String salon;
	private String scena;
	private int redove;
	/**
	 * @return the salon
	 */
	public String getSalon() {
		return salon;
	}
	/**
	 * @param salon the salon to set
	 */
	public void setSalon(String salon) {
		this.salon = salon;
	}
	/**
	 * @return the scena
	 */
	public String getScena() {
		return scena;
	}
	/**
	 * @param scena the scena to set
	 */
	public void setScena(String scena) {
		this.scena = scena;
	}
	/**
	 * @return the redove
	 */
	public int getRedove() {
		return redove;
	}
	/**
	 * @param redove the redove to set
	 */
	public void setRedove(int redove) {
		this.redove = redove;
	}
	
	
}

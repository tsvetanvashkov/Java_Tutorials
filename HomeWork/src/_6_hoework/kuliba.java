package _6_hoework;

public class kuliba {

	
	private String stena;
	private String pokriv;
	private String vrata;
	
	//konstructor
	
	public kuliba(String vrata){
		super();
		
		this.vrata = vrata;
	}

	/**
	 * @return the stena
	 */
	public String getStena() {
		return stena;
	}
	/**
	 * @param stena the stena to set
	 */
	public void setStena(String stena) {
		this.stena = stena;
	}
	/**
	 * @return the pokriv
	 */
	public String getPokriv() {
		return pokriv;
	}
	/**
	 * @param pokriv the pokriv to set
	 */
	public void setPokriv(String pokriv) {
		this.pokriv = pokriv;
	}
	/**
	 * @return the vrata
	 */
	public String getVrata() {
		return vrata;
	}
	/**
	 * @param vrata the vrata to set
	 */
	public void setVrata(String vrata) {
		this.vrata = vrata;
	}

	
	
	
	
}
